package Strings.src;

public class FormattedString {
    public static void main(String[] args) {
        System.out.println("Manikanth Daru");
        System.out.printf("\n\"%s\"\n","Manikanth Daru");
        System.out.println(1);
        System.out.printf("0%2d%2d\n",1,2);
        System.out.println(3.56789);
        System.out.printf("%.2f\n",3.56789);
        System.out.println('M');
        System.out.printf("\'%c\'",'M');
    }
}
